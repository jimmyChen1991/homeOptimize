package com.hhyg.TyClosing.info;

public class SkuAttr {
	private int attrId;
	private String attrVaule;
	
	
	public SkuAttr(int attrId, String attrVaule) {
		this.attrId = attrId;
		this.attrVaule = attrVaule;
	}
	public int getAttrId() {
		return attrId;
	}
	public void setAttrId(int attrId) {
		this.attrId = attrId;
	}
	public String getAttrVaule() {
		return attrVaule;
	}
	public void setAttrVaule(String attrVaule) {
		this.attrVaule = attrVaule;
	}
	
	
}
